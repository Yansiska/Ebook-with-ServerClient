    private void update() {
        try {
            obj1.setIdemployee(txt_idemployee.getText().trim());
            obj1.setName(txt_name.getText().trim());
            obj1.setPosition(txt_position.getText().trim());
            int i = obj1.doUpdate();
            if(i > 0) {
                JOptionPane.showMessageDialog(null, "Data has been successfully updated");
                this.clear();
            }
            else {
                JOptionPane.showMessageDialog(null, "Data cant be updated");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error: " + e);
        }
    }
      
       private void delete() {
        try {
            obj1.setIdemployee(txt_idemployee.getText());            
            int i = obj1.doDelete();
            if(i > 0) {
                JOptionPane.showMessageDialog(null, "Data has been successfully deleted");
                this.clear();
            }
            else {
                JOptionPane.showMessageDialog(null, "Data cant be deleted");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error: " + e);
        }
    }

