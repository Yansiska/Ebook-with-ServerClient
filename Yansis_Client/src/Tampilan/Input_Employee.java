    private void clear()
    {
        txt_idemployee.setText("");
        txt_name.setText("");
        txt_position.setText("");
    }
    
    
    private void add(){
        try{
            obj1.setIdemployee(txt_idemployee.getText());
            obj1.setName(txt_name.getText());
            obj1.setPosition(txt_position.getText());
            
            if(obj1.getIdemployee().equals("") && obj1.getName().equals("") && obj1.getPosition().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Data cant be empty");
            }
            else
            {
                int i = obj1.doInsert();
                if(i > 0){
                    JOptionPane.showMessageDialog(null, "Data has been successed");
                    this.clear();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Data cant be added");
                }
            }
        }
        catch (Exception e)
            {
               JOptionPane.showMessageDialog(null,"error 1 :"+ e);
            }
    }
            
    private void getRecord(String id)
    {
        try{
            obj1.setIdemployee(id);
            ArrayList data = obj1.getRecord();
            txt_idemployee.setText((String)data.get(0));
            txt_name.setText((String)data.get(1));
            txt_position.setText((String)data.get(2));
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Data cant be displayed");
        }
    }
    
    public void autoGenerateID()
    {
        try
        {
            String ID_Employee = obj3.autoidEmployee();
            txt_idemployee.setText(ID_Employee);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan" + ex.getMessage());
   
        }
    }
    
